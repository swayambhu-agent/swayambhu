  lines.push("  const binary = atob(normalized);");
  lines.push("  const bytes = Uint8Array.from(binary, (char) => char.charCodeAt(0));");
  lines.push("  return new TextDecoder().decode(bytes);");
  lines.push("}");
  lines.push("");

