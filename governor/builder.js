  lines.push("");
  lines.push("    const nonce = Math.random().toString(36).slice(2, 6);");
  lines.push("    const ts = Date.now().toString().padStart(15, '0');");
  lines.push("    const eventKey = `event:${ts}:inbound_message:${nonce}`;");
  lines.push("    const commTurn = ingestInbound(channel, inbound);");
  lines.push("    await env.KV.put(eventKey, JSON.stringify({");
