  lines.push("              turn.reply_target = conv.reply_target;");
  lines.push("            } else {");
  lines.push('              const parts = item.conversation_id.replace("chat:", "").split(":");');
  lines.push('              turn.reply_target = { platform: parts[0], channel: parts.slice(1).join(":"), thread_ts: null };');
