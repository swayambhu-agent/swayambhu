  lines.push("              } else {");
  lines.push("                await K.kvWriteSafe(item.key, item);");
  lines.push("              }");
  lines.push("            }");
  lines.push("          } catch (err) {");
  lines.push('            await K.karmaRecord({ event: "outbox_error", id: item.id, error: err.message });');
  lines.push("          }");
  lines.push("        }");
  lines.push("      },");
  lines.push("    },");
  lines.push("  },");
